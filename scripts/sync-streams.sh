#!/usr/bin/env bash

# Move to the repo root (assuming this script lives in ./scripts/)
cd "$(dirname "$0")/.."

# Fetch updates from iptv-org
git fetch iptv-org

# Sync all *.m3u files from iptv-org to ./streams/
git checkout iptv-org/master -- streams/*.m3u

# If streams/index.m3u accidentally exists in the repo, untrack it (ignore errors if it doesn't)
git rm --cached --ignore-unmatch streams/index.m3u || true

# Add and commit any changes to streams/*.m3u (skip if no changes)
git add streams/*.m3u
git diff --cached --quiet || git commit -m "Sync all streams/*.m3u from iptv-org"

# Generate a combined index.m3u (root-level, not in streams/)
echo "#EXTM3U" > index.m3u
echo "# Combined playlist generated by GitHub Actions" >> index.m3u
echo "" >> index.m3u

# Automatically include all US-related .m3u files, plus selected others
SELECTED_FILES=(streams/us*.m3u streams/ca.m3u streams/uk.m3u streams/au.m3u)

for FILE in "${SELECTED_FILES[@]}"; do
  if [ -f "$FILE" ]; then
    # Remove redundant #EXTM3U lines
    grep -v '^#EXTM3U' "$FILE" >> index.m3u
    echo "" >> index.m3u
  else
    echo "Warning: $FILE not found!" >&2
  fi
done

# Add and commit the updated index.m3u (skip if no changes)
git add index.m3u
git diff --cached --quiet || git commit -m "Update index.m3u with combined playlist"
