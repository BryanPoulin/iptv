#!/usr/bin/env bash
set -e
set -x

cd "$(dirname "$0")/.."

# Fetch updates from iptv-org
git fetch iptv-org

# Overwrite the local streams/ folder with upstream's streams/
git checkout iptv-org/master -- streams/

# Stage the streams changes
git add streams/

# If there are changes to the streams folder, commit them
git commit -m "Sync streams folder with iptv-org" || echo "No changes to commit for streams"

# Generate the index.m3u
SELECTED_FILES=("ca.m3u" "us.m3u" "uk.m3u")
echo "#EXTM3U" > index.m3u
echo "# This is a combined playlist generated by GitHub Actions" >> index.m3u
echo "" >> index.m3u

for FILE in "${SELECTED_FILES[@]}"; do
  if [ -f "streams/$FILE" ]; then
    echo "# --- START $FILE ---" >> index.m3u
    cat "streams/$FILE" >> index.m3u
    echo "# --- END $FILE ---" >> index.m3u
    echo "" >> index.m3u
  else
    echo "Warning: streams/$FILE not found!" >&2
  fi
done

# Stage and commit index.m3u if changed
git add index.m3u
git diff --cached --quiet index.m3u || git commit -m "Update index.m3u with combined playlist"
