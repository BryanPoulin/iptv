#!/usr/bin/env bash
# scripts/sync-streams.sh

set -e
cd "$(dirname "$0")/.."

# Sync all .m3u files from iptv-org's /streams/
echo "Fetching updates from iptv-org..."
git fetch iptv-org
git checkout iptv-org/master -- streams/

# Stage updated streams/ folder
git add streams/
git commit -m "Sync streams folder with iptv-org" || echo "No changes to commit"

# List of .m3u files to combine
SELECTED_FILES=("ca.m3u" "us.m3u" "uk.m3u" "au.m3u")

# Create combined index.m3u
echo "#EXTM3U" > index.m3u
echo "# Combined playlist generated by GitHub Actions" >> index.m3u
echo "" >> index.m3u

for FILE in "${SELECTED_FILES[@]}"; do
  if [ -f "streams/$FILE" ]; then
    echo "# --- START $FILE ---" >> index.m3u
    # Remove #EXTM3U from the file to avoid duplicates
    grep -v '^#EXTM3U' "streams/$FILE" >> index.m3u
    echo "# --- END $FILE ---" >> index.m3u
    echo "" >> index.m3u
  else
    echo "Warning: streams/$FILE not found!" >&2
  fi
done

# Show differences for debug
git diff index.m3u || true

# Stage and commit index.m3u if there are changes
git add index.m3u
git commit -m "Update index.m3u with combined playlist" || echo "No changes to commit"
